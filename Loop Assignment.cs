
while statement

 

class WhileDemo {
   public static void main(String[] args){
       int count = 1;
       while (count < 11) {
           System.out.println("Count is: " + count);
           count++;
       }
   }
}


 do while statement

 

class DoWhileDemo {
   public static void main(String[] args)


{
       int count = 1;
       do {
           System.out.println("Count is: " + count);
           count++;
       } while (count < 11);
   }
}